
class Calulator {

	public static void main(String[] args) 
	{
		//Substract two numbers and print the difference.
		Integer a, b, c //Define the variables.
		a = args[0].toInteger() //Get the first number.
		b = args[1].toInteger() //Get the second number.
		c = a - b //Add 2 numbers.
		println "The difference of two numbers =" + c //Show output.
	}

}
